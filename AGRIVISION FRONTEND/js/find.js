function showCompanies() {
    const location = document.getElementById("location").value;
    const detailsDiv = document.getElementById("company-details");
    detailsDiv.innerHTML = '';

    if (location && companies[location]) {
        companies[location].forEach(company => {
            const companyInfo = `
                <div class="company">
                    <h2>${company.name}</h2>
                    <p><strong>Description:</strong> ${company.description}</p>
                    <p><strong>Address:</strong> ${company.address}</p>
                    <p><strong>Phone:</strong> ${company.phone}</p>
                    <p><a href="${company.website}" target="_blank">Visit Website</a></p>
                    <div class="map">${company.map}</div>
                </div>
            `;
            detailsDiv.innerHTML += companyInfo;
        });
    } else {
        detailsDiv.innerHTML = '<p>No companies found for this location.</p>';
    }
}
